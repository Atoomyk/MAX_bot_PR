def get_specialty_name(code: str) -> str:
    """
    Возвращает название специальности по коду.
    Если код не найден, возвращает сам код.
    """
    try:
        code_int = int(code)
        return SPECIALTY_CODES.get(code_int, str(code))
    except (ValueError, TypeError):
        return str(code)
